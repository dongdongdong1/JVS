public class ResultInfo {
    private int code;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    @Override
    public String toString() {
        return "ResultInfo{" +
                "code=" + code +
                '}';
    }
}
